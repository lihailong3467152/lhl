# ===================== 评分核心 =====================
def score_one(pdf_path):
    fname = os.path.basename(pdf_path)
    unit = fname.replace(".pdf","")

    try:
        full_text = ""
        with pdfplumber.open(pdf_path) as pdf:
            for p in pdf.pages:
                t = p.extract_text()
                if t: full_text += t
    except:
        return unit, {}, 0,0,0,0,0, ["PDF读取失败"]

    text = full_text
    problems = []
    scores = {}
    dim = {"及时性":0,"完整性":0,"细化程度":0,"规范性":0}

    # ---------------- 1-3 及时性：默认满分 ----------------
    scores["1.公开时限合规性"] = 6
    scores["2.集中公开情况"] = 2
    scores["3.年度完成时间"] = 2
    dim["及时性"] = 10

    # ---------------- 4-13 完整性：默认满分，缺则扣分 ----------------
    scores["4.四本预算公开完整性"] = 5
    scores["5.收支内容完整性"] = 5
    scores["6.所属单位公开覆盖率"] = 4
    scores["7.三公经费公开完整性"] = 4 if "三公" in text else 0
    scores["8.机关运行经费公开"] = 3 if "机关运行经费" in text else 0
    scores["9.政府采购信息公开"] = 3 if "政府采购" in text else 0
    scores["10.国有资产信息公开"] = 3 if "国有资产" in text else 0
    scores["11.专项资金信息公开"] = 3 if "专项" in text else 0
    scores["12.绩效信息公开"] = 3 if "绩效" in text else 0
    scores["13.重点事项说明完整性"] = 2
    dim["完整性"] = sum([
        5,5,4, scores["7.三公经费公开完整性"],
        scores["8.机关运行经费公开"], scores["9.政府采购信息公开"],
        scores["10.国有资产信息公开"], scores["11.专项资金信息公开"],
        scores["12.绩效信息公开"],2
    ])

    # ---------------- 14-22 细化程度 ----------------
    scores["14.支出功能分类细化程度"] = 5
    scores["15.基本支出经济分类细化程度"] = 5
    scores["16.三公经费明细细化程度"] = 4 if "三公" in text else 0

    # 17. 项目支出细化程度：包含多个“预算”表 → 满分
    if text.count("预算") >= 2:
        scores["17.项目支出细化程度"] = 4
    else:
        scores["17.项目支出细化程度"] = 0
        problems.append("17.项目支出细化程度：未包含多个预算表，扣4分")

    # 18. 绩效指标细化程度：有绩效表 → 满分
    scores["18.绩效指标细化程度"] = 3 if "绩效" in text else 0

    # 19. 收支差异说明：有绩效表 → 满分
    scores["19.收支差异说明细化程度"] = 3 if "绩效" in text else 0

    scores["20.三公经费变动说明细化程度"] = 2 if "三公" in text else 0
    scores["21.空白项说明完整性"] = 2
    scores["22.民生项目信息细化程度"] = 2
    dim["细化程度"] = sum([
        5,5,4, scores["17.项目支出细化程度"],
        scores["18.绩效指标细化程度"], scores["19.收支差异说明细化程度"],
        scores["20.三公经费变动说明细化程度"],2,2
    ])

    # ---------------- 23-32 规范性 ----------------
    scores["23.公开位置规范性"] = 4
    scores["24.统一平台同步公开"] = 3
    scores["25.公开格式规范性"] = 3
    scores["26.内容可检索性"] = 2
    scores["27.数据一致性"] = 4
    scores["28.数据勾稽关系正确性"] = 3
    scores["29.涉密内容处理规范性"] = 2
    scores["30.信息长期留存"] = 1

    # 31. 名称规范性：年份 + 单位/部门 + 预算/决算 → 满分
    has_year = bool(re.search(r"202\d", fname))
    has_unit = "厅" in fname or "局" in fname or "部门" in fname
    has_budget = "预算" in fname or "决算" in fname
    if has_year and has_unit and has_budget:
        scores["31.名称规范性"] = 2
    else:
        scores["31.名称规范性"] = 0
        problems.append("31.名称规范性：缺少年份/单位/预算，扣2分")

    scores["32.版本一致性"] = 1
    dim["规范性"] = sum([4,3,3,2,4,3,2,1, scores["31.名称规范性"],1])

    # 总分
    total = round(sum(dim.values()),2)
    return unit, scores, dim["及时性"], dim["完整性"], dim["细化程度"], dim["规范性"], total, " | ".join(problems)

# ===================== 批量执行 =====================
def run():
    rows = []
    for f in os.listdir(PDF_FOLDER):
        if f.lower().endswith(".pdf"):
            u, s, t, c, d, n, total, p = score_one(os.path.join(PDF_FOLDER, f))
            row = {
                "单位名称":u, "及时性总分":t, "完整性总分":c, "细化程度总分":d, "规范性总分":n
            }
            row.update(s)
            row["问题记录"] = p
            row["评估总分"] = total
            rows.append(row)
            print(f"✅ {u} | 总分：{total}")

    df = pd.DataFrame(rows)
    df = df[HEADER]
    df.to_excel(OUTPUT_EXCEL, index=False)
    print(f"\n🎉 完成！文件：{OUTPUT_EXCEL}")

if __name__ == "__main__":
    run()